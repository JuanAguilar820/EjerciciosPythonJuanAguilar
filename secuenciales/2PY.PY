  # Definicion de algoridmo de calculacion el perimetro y el area de un rectangulo 
Base= int(input("Ingresa la base del rectangulo: "))
Altura = int(input("ingresa la altura del rectangulo: "))

Perimetro= 2 * (Base + Altura)
Area= Base *  Altura

